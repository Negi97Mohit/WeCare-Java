    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int row = enterpriseJTable.getSelectedRow();

        if(row <0)
        {
            JOptionPane.showMessageDialog(null,"Please select any row!","Warning",JOptionPane.WARNING_MESSAGE );
        }
        Enterprise  enterprise = (Enterprise)enterpriseJTable.getValueAt(row,0);
        Network network = (Network)enterpriseJTable.getValueAt(row,1);
        
        network.getEnterpriseDirectory().getEnterpriseList().remove(enterprise);

        JOptionPane.showMessageDialog(null, "Enterprise has been deleted!","Success", JOptionPane.PLAIN_MESSAGE);
        populateTable();
     
        dB4OUtil.storeSystem(system);
    }//GEN-LAST:event_deleteButtonActionPerformed

