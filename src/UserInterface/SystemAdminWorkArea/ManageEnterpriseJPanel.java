    private void AddJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddJButtonActionPerformed

        Network network = (Network) NetworkComboBox.getSelectedItem();
        Enterprise.EnterpriseType enterpriseType = (Enterprise.EnterpriseType) EnterpriseComboBox.getSelectedItem();
        String enterpriseName = enterpriseNameTextField.getText();
        
        populateTable();
     
        if(enterpriseName.isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter enterprise name!","Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
       
        for(Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()){
            if(enterprise.getName().equals(enterpriseName)){
                JOptionPane.showMessageDialog(null, "Enterprise already exist!","Warning",JOptionPane.WARNING_MESSAGE);
                return;
            }
        }
        
        Enterprise enterprise = network.getEnterpriseDirectory().createAndAddEnterprise(enterpriseName, enterpriseType);
        JOptionPane.showMessageDialog(null, "Enterprise added successfully!","Success", JOptionPane.PLAIN_MESSAGE);
        populateTable();
        
        dB4OUtil.storeSystem(system);
        enterpriseNameTextField.setText("");
        
    }//GEN-LAST:event_AddJButtonActionPerformed
  
