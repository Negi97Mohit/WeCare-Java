    private void populateOrderTable(){
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);
        for (Order o : system.getOrderDirectory().getOrderList()){
            if(o.getStatus().equals("Ordered")&&o.getRName().equals(enterprise.getName())){
                Object[] row = new Object[5];
                row[0] = o;
                row[1] = o.gettPrice();
                row[2] = o.getRName();
                row[3] = o.getDName();
                row[4] = o.getStatus();
                model.addRow(row);
            
            
        }
    }
    }
    void populatemodifyMenuJTable(){
        DefaultTableModel model = (DefaultTableModel) menuJTable.getModel();
        model.setRowCount(0);
        for (ModifyMedicinesRequest m : enterprise.getModifyDishesRequestDirectory().getModifyMenuRequestList()){
            if(m.getModifyStatus().equals("Processing")){
            Object[] row = new Object[5];
            row[0] = m.getMedicine().getMedicineName();
            row[1] = m.getoPrice();
            row[2] = m.getFp();
            row[3] = m;
            row[4] = m.getModifyStatus();
            model.addRow(row);
            }
        }
    }
    
    void populatePurchaseTable(){
        DefaultTableModel model = (DefaultTableModel) purchaseJTable.getModel();
        model.setRowCount(0);
        for (PurchaseRequest p : enterprise.getPurchaseRequestDirectory().getPurchaseRequestList()){
            if(p.getMedicineRequest().equals("Processing")){
                Object[] row = new Object[4];
                row[0] = p.getMedicineName();
                row[1] = p.gettRate();
                row[2] = p;
                row[3] = p.getMedicineRequest();
                model.addRow(row);
            }
            
        }
    }
