    private void btnMApproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMApproveActionPerformed
        // TODO add your handling code here:
        int selectedrow = menuJTable.getSelectedRow();

        if(selectedrow >=0)
        {
            ModifyMedicinesRequest  vs = (ModifyMedicinesRequest)menuJTable.getValueAt(selectedrow,3);
            
            vs.setModifyStatus("Approved");
            
            vs.getMedicine().setPrice(vs.getFp());
            
            
            JOptionPane.showMessageDialog(null, "This request has been approved!!");
            populatemodifyMenuJTable();
        }
        else
        JOptionPane.showMessageDialog(null,"Please select an request!! ");
    }//GEN-LAST:event_btnMApproveActionPerformed

